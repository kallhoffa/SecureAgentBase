export const getStartupScript = (useBundle = false) => {
  const bundleSection = useBundle ? `
# Try to download pre-bundled packages from GCS for faster setup
if [ "$USE_BUNDLE" = "true" ]; then
  echo "Downloading pre-bundled packages from GCS..."
  if curl -sf --connect-timeout 15 "${GCS_BUNDLE_URL}" -o /tmp/packages.tar.gz 2>/dev/null && \\
     curl -sf --connect-timeout 15 "${GCS_SIGNATURE_URL}" -o /tmp/packages.tar.gz.asc 2>/dev/null; then
    echo "Bundle downloaded, verifying signature..."
    
    # Import trusted GPG key
    GNUPGHOME=/tmp/gpghome
    mkdir -p $GNUPGHOME
    chmod 700 $GNUPGHOME
    echo '${BUNDLE_SIGNER_KEY}' | gpg --import --no-tty 2>/dev/null || true
    
    # Verify signature
    if gpg --batch --verify /tmp/packages.tar.gz.asc /tmp/packages.tar.gz 2>/dev/null; then
      echo "Bundle signature verified!"
      tar -xzf /tmp/packages.tar.gz -C /opt/ 2>/dev/null || true
      
      # Install .deb packages if present
      if [ -d /opt/packages ]; then
        echo "Installing from verified bundle..."
        cd /opt/packages
        dpkg -i *.deb 2>/dev/null || apt-get install -f -y --no-install-recommends 2>/dev/null || true
      fi
      
      # Add bundled Node.js to PATH
      if [ -d /opt/nodejs/bin ]; then
        export PATH="/opt/nodejs/bin:$PATH"
        echo "Bundle Node.js available"
      fi
      # Add bundled OpenCode to PATH
      if [ -d /opt/opencode ]; then
        export PATH="/opt/opencode:$PATH"
        echo "Bundle OpenCode available"
      fi
      
      export BUNDLE_SUCCESS="true"
    else
      echo "WARNING: Bundle signature verification failed! Using standard installation..."
    fi
  else
    echo "WARNING: Could not download bundle. Using standard installation..."
  fi
fi
` : '';

  return `#!/bin/bash
# Allow unset variables (don't use -u) to prevent early exit
# Continue on errors so kimaki systemd service gets created
set +e
export HOME=/root
export DEBIAN_FRONTEND=noninteractive
${bundleSection}
# Generate unique repo name to avoid collisions
SUFFIX=$(echo $RANDOM | md5sum | head -c 6)
REPO_NAME="agentbase-$SUFFIX"

# Use environment variables passed via metadata
REPO_OWNER="kallhoffa"
FIREBASE_STAGING=$(curl -s "http://metadata.google.internal/computeMetadata/v1/instance/attributes/firebase_staging" -H "Metadata-Flavor: Google")
FIREBASE_PRODUCTION=$(curl -s "http://metadata.google.internal/computeMetadata/v1/instance/attributes/firebase_production" -H "Metadata-Flavor: Google")

echo "DEBUG: REPO_OWNER=$REPO_OWNER"
echo "DEBUG: FIREBASE_STAGING=$FIREBASE_STAGING"
echo "DEBUG: FIREBASE_PRODUCTION=$FIREBASE_PRODUCTION"

PASSPHRASE=$(curl -s "http://metadata.google.internal/computeMetadata/v1/instance/attributes/encryption_passphrase" -H "Metadata-Flavor: Google")
echo "DEBUG: PASSPHRASE length=${'${#PASSPHRASE}'}"

GITHUB_PAT=$(curl -s "http://metadata.google.internal/computeMetadata/v1/instance/attributes/github_pat" -H "Metadata-Flavor: Google")
DISCORD_BOT_TOKEN=$(curl -s "http://metadata.google.internal/computeMetadata/v1/instance/attributes/discord_bot_token" -H "Metadata-Flavor: Google")
DISCORD_GUILD_ID=$(curl -s "http://metadata.google.internal/computeMetadata/v1/instance/attributes/discord_guild_id" -H "Metadata-Flavor: Google")

sudo apt-get update -y || true
sudo apt-get install -y curl git jq apt-transport-https ca-certificates gnupg2 ufw || true

# Install Node.js 20.x
curl -fsSL https://deb.nodesource.com/setup_20.x | sudo -E bash - || true
sudo apt-get install -y nodejs || true

# Install GitHub CLI
type gh >/dev/null 2>&1 || (curl -fsSL https://cli.github.com/packages/githubcli-archive-keyring.gpg | sudo dd of=/usr/share/keyrings/githubcli-archive-keyring.gpg && echo "deb [arch=$(dpkg --print-architecture) signed-by=/usr/share/keyrings/githubcli-archive-keyring.gpg] https://cli.github.com/packages stable main" | sudo tee /etc/apt/sources.list.d/github-cli.list > /dev/null && sudo apt-get update -y && sudo apt-get install -y gh) || true

# Install Kimaki
if command -v npm &> /dev/null; then
  npm install -g kimaki@latest || true
fi

# Initialize git repo
cd /root
mkdir -p $REPO_NAME
cd $REPO_NAME
git init
git checkout -b main

# Configure git
git config user.email "agent@secureagentbase.com"
git config user.name "SecureAgent Manager"

# Create .gitignore
cat > .gitignore << 'GITEOF'
node_modules/
.env*
dist/
build/
*.log
GITEOF

# TODO: Clone the actual fork/template here
echo "placeholder" > README.md
git add .
git commit -m "Initial commit"

# Create GitHub repo and push
if [ -n "$GITHUB_PAT" ]; then
  echo $GITHUB_PAT | gh auth login --with-token
  
  # Create new repo
  if gh repo view "\${REPO_OWNER}/\${REPO_NAME}" 2>/dev/null; then
    echo "Repo already exists, updating remote..."
    git remote set-url origin "https://\${REPO_OWNER}:$GITHUB_PAT@github.com/\${REPO_OWNER}/\${REPO_NAME}.git" 2>/dev/null || true
  else
    gh repo create "$REPO_NAME" --public --source=. --push || { echo "Repo create failed!"; exit 1; }
    git remote add origin "https://\${REPO_OWNER}:$GITHUB_PAT@github.com/\${REPO_OWNER}/\${REPO_NAME}.git" 2>/dev/null || true
  fi

  git push -u origin main 2>/dev/null || echo "Push may have failed, continuing..."
  
  echo "DEBUG: Setting GitHub secrets..."
  gh secret set FIREBASE_STAGING_PROJECT_ID --body "$FIREBASE_STAGING" -R "\${REPO_OWNER}/\${REPO_NAME}" 2>/dev/null || echo "WARNING: Failed to set FIREBASE_STAGING_PROJECT_ID"
  gh secret set FIREBASE_PRODUCTION_PROJECT_ID --body "$FIREBASE_PRODUCTION" -R "\${REPO_OWNER}/\${REPO_NAME}" 2>/dev/null || echo "WARNING: Failed to set FIREBASE_PRODUCTION_PROJECT_ID"
  echo "DEBUG: GitHub secrets set"
fi

# Install Kimaki globally and create its configuration
KIMAKI_CONFIG_DIR=/root/.kimaki
mkdir -p $KIMAKI_CONFIG_DIR
cat > $KIMAKI_CONFIG_DIR/config.yml << 'KIMAKICONF'
# Kimaki config - auto-generated
openai_api_key: ""
github_token: ""
KIMAKICONF

# Create systemd service for Kimaki
cat > /etc/systemd/system/kimaki.service << 'SERVICEEOF'
[Unit]
Description=Kimaki Agent Service
After=network.target

[Service]
Type=simple
User=root
WorkingDirectory=/root/$REPO_NAME
ExecStart=/usr/bin/node $(which kimaki) start
Restart=on-failure
RestartSec=10
Environment="GITHUB_PAT=$GITHUB_PAT"
Environment="DISCORD_BOT_TOKEN=$DISCORD_BOT_TOKEN"
Environment="DISCORD_GUILD_ID=$DISCORD_GUILD_ID"
Environment="PASSPHRASE=$PASSPHRASE"
Environment="FIREBASE_STAGING=$FIREBASE_STAGING"
Environment="FIREBASE_PRODUCTION=$FIREBASE_PRODUCTION"
Environment="NODE_ENV=production"

[Install]
WantedBy=multi-user.target
SERVICEEOF

systemctl daemon-reload
systemctl enable kimaki.service
systemctl start kimaki.service

echo "=== Kimaki installation complete! ==="
echo "Service status:"
systemctl status kimaki.service --no-pager || true
`;
};
